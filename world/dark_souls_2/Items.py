@dataclass
class ItemData:
    code: int
    """The Archipelago code for this item."""

    name: str
    """The Archipelago name for this item."""

    category: ItemCategory
    """The category that this item is part of."""

    classification: ItemClassification = ItemClassification.filler
    """How important this item is to the game progression."""

    dlc: Optional[bool] = False
    """Whether this item is only found in one of the DLCs."""

    sotfs: Optional[bool] = False
    """Whether this item is only found in the Scholar of the First Sin version of the game."""

    skip: Optional[bool] = False
    """Whether to omit this item from randomization and replace it with other items."""

    bundle: Optional[bool] = False
    """
    Whether the amount of this item is greater than 1.
    The item amount is added to the item code so, for example,
    an item with code 1000 will become 1005 if it is a bundle of 5 items
    """

    max_reinforcement: int = 0
    """The max reinforcement level for this item."""

    reinforcement: int = 0
    """The reinforcement level for this item."""

    def __post_init__(self):
        if re.search(r' x\d+$', self.name):
            self.bundle = True

